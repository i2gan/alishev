public class Lesson_23_1 {
    public static void main(String[] args) {
        System.out.printf("This is a string %s", "NICE");
        System.out.printf("\nThis is a string %d", 10);
        System.out.printf("\nThis is a string %10d", 123);
        System.out.printf("\nThis is a string %-10d", 123);
        System.out.printf("\nThis is a string %.2f", 3.1);

        /*
        This is a string NICE
        This is a string 10
        This is a string        123
        This is a string 123
        This is a string 3,10
         */
    }
}
